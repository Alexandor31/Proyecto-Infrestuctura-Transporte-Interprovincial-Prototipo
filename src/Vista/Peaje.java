    private void jBconsultarPeajeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBconsultarPeajeActionPerformed
        PeajeO peajeConsulta = new PeajeO();
        peajeConsulta.buscarPorNombre(jTFnombreConsultaPeaje.getText());
        String selecion = (String) jCBconsultasPeaje.getSelectedItem();
        jTAconsultaPeaje.setText(selecion);
        switch (selecion) {
            case "Ubicaci√≥n":
                jTAconsultaPeaje.setText(peajeConsulta.getUbicacion());
                break;
            case "Cuidad":
                jTAconsultaPeaje.setText(peajeConsulta.getCiudad());
                break;
            case "Numero de Cabinas":
                jTAconsultaPeaje.setText(""+peajeConsulta.getNumCabinas());
                break;
            case "Estado":
                jTAconsultaPeaje.setText(""+peajeConsulta.getEstado());
                break;
        }
    }//GEN-LAST:event_jBconsultarPeajeActionPerformed

