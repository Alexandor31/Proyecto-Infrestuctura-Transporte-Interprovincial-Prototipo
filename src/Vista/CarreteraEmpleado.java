    private void jBconsultarCarreteraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBconsultarCarreteraActionPerformed
        CarreteraO carreteraConsulta = new CarreteraO();
        carreteraConsulta.buscarPorNombre(jTFnombreCarreteraConsulta.getText());
        String selecion = (String) jCBconsultasCarretera.getSelectedItem();
        jTAcosultaCarretera.setText(selecion);
        switch (selecion) {
            case "Ubicaci贸n":
                jTAcosultaCarretera.setText(carreteraConsulta.getUbicacion());
                break;
            case "Numero de Carriles":
                jTAcosultaCarretera.setText(""+carreteraConsulta.getNumcarriles());
                break;
            case "Estado":
                jTAcosultaCarretera.setText(""+carreteraConsulta.getEstado());
                break;
        }
    }//GEN-LAST:event_jBconsultarCarreteraActionPerformed

