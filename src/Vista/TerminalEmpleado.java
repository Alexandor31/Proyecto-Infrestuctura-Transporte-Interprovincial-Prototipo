    private void jBconsultarTerminalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBconsultarTerminalActionPerformed
        TerminalO terminalConsulta = new TerminalO();
        terminalConsulta.buscarPorNombre(jTFnombreConsultaTerminal.getText());
        String selecion = (String) jCBconsultasTerminal.getSelectedItem();
        jTAconsultaTerminal.setText(selecion);
        switch (selecion) {
            case "Ubicaci贸n":
                jTAconsultaTerminal.setText(terminalConsulta.getUbicacion());
                break;
            case "Cuidad":
                jTAconsultaTerminal.setText(terminalConsulta.getCiudad());
                break;
            case "Numero de Adenes":
                jTAconsultaTerminal.setText(""+terminalConsulta.getAndenes());
                break;
            case "Estado":
                jTAconsultaTerminal.setText(""+terminalConsulta.getEstado());
                break;
            case "Capacidad":
                jTAconsultaTerminal.setText(""+terminalConsulta.getCapacidad());
                break;
        }
    }//GEN-LAST:event_jBconsultarTerminalActionPerformed

